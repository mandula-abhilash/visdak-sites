export function getThemesByCategory(category) {
  if (category === "all") return builderThemes;
  return builderThemes.filter((theme) => theme.category === category);
}

export function getThemeById(id) {
  return builderThemes.find((theme) => theme.id === id);
}

// Utility function to get CSS custom properties for a theme
export function getThemeCSSVariables(theme, mode = "light") {
  const colors = theme[mode];
  return {
    "--color-primary": colors.primary,
    "--color-accent": colors.accent,
    "--color-background": colors.background,
    "--color-surface": colors.surface,
    "--color-text": colors.text,
    "--color-text-secondary": colors.textSecondary,
    "--color-border": colors.border,
    "--font-family": colors.font,
  };
}

// Color accessibility utilities
export function getContrastRatio(color1, color2) {
  // Simplified contrast ratio calculation
  // For production, use a proper color library like chroma.js
  const getLuminance = (hex) => {
    const rgb = parseInt(hex.slice(1), 16);
    const r = (rgb >> 16) & 0xff;
    const g = (rgb >> 8) & 0xff;
    const b = (rgb >> 0) & 0xff;
    return 0.2126 * r + 0.7152 * g + 0.0722 * b;
  };

  const lum1 = getLuminance(color1);
  const lum2 = getLuminance(color2);
  return (Math.max(lum1, lum2) + 0.05) / (Math.min(lum1, lum2) + 0.05);
}

export function isAccessible(backgroundColor, textColor) {
  return getContrastRatio(backgroundColor, textColor) >= 4.5;
}
